def generate_social_nodes(seed: int = SEED) -> list[str]:
    """Return Cypher CREATE statements for social graph nodes only."""
    rng = random.Random(seed)
    lines: list[str] = []

    lines.append("// mini_social.cypher — generated by scripts/generate_fixtures.py")
    lines.append(f"// seed={seed}")
    lines.append(f"// nodes: Person={N_PERSONS} Company={N_COMPANIES} Post={N_POSTS}")
    lines.append("// edges are created by the Rust test code (see regression_real_world.rs)")
    lines.append("")

    # ── Person nodes ────────────────────────────────────────────────────────────
    lines.append("// ── Person nodes ─────────────────────────────────────────────")
    for i in range(N_PERSONS):
        name = (
            rng.choice(FIRST_NAMES)
            + " "
            + rng.choice(LAST_NAMES)
            + f"_{i}"
        )
        age = rng.randint(18, 72)
        city = rng.choice(CITIES)
        email = f"user{i}@example.com"
        props = cypher_props({"name": name, "age": age, "city": city, "email": email})
        lines.append(f"CREATE (n:Person {props});")

    lines.append("")

    # ── Company nodes ───────────────────────────────────────────────────────────
    lines.append("// ── Company nodes ────────────────────────────────────────────")
    for j in range(N_COMPANIES):
        name = COMPANY_NAMES_LIST[j % len(COMPANY_NAMES_LIST)] + f" {j}"
        industry = rng.choice(INDUSTRIES)
        founded = rng.randint(1970, 2023)
        props = cypher_props({"name": name, "industry": industry, "founded": founded})
        lines.append(f"CREATE (n:Company {props});")

    lines.append("")

    # ── Post nodes ──────────────────────────────────────────────────────────────
    lines.append("// ── Post nodes ───────────────────────────────────────────────")
    for k in range(N_POSTS):
        topic = rng.choice(TOPICS)
        template = rng.choice(POST_TEMPLATES)
        content = template.format(topic=topic)
        year = rng.randint(2020, 2024)
        month = rng.randint(1, 12)
        day = rng.randint(1, 28)
        created_at = f"{year}-{month:02d}-{day:02d}"
        likes = rng.randint(0, 5000)
        props = cypher_props({"content": content, "created_at": created_at, "likes": likes})
        lines.append(f"CREATE (n:Post {props});")

    return lines


