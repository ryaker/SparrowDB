def generate_knowledge_nodes(seed: int = SEED) -> list[str]:
    """Return Cypher CREATE statements for knowledge graph nodes only."""
    rng = random.Random(seed + 1000)  # different substream from social graph
    lines: list[str] = []

    lines.append("// mini_knowledge.cypher — generated by scripts/generate_fixtures.py")
    lines.append(f"// seed={seed}")
    lines.append(f"// nodes: Fact={N_FACTS} Entity={N_ENTITIES}")
    lines.append("// edges are created by the Rust test code (see regression_real_world.rs)")
    lines.append("")

    # ── Fact nodes ──────────────────────────────────────────────────────────────
    lines.append("// ── Fact nodes ───────────────────────────────────────────────")
    for i in range(N_FACTS):
        entity = rng.choice(ENTITIES_KG)
        concept = rng.choice(CONCEPTS)
        template = rng.choice(FACT_TEMPLATES)
        content = template.format(
            entity=entity,
            concept=concept,
            year=rng.randint(1990, 2024),
            domain=rng.choice(CONCEPTS),
            metric=rng.choice(["latency", "throughput", "accuracy", "recall"]),
            pct=rng.randint(5, 80),
            relation=rng.choice(["inheritance", "composition", "delegation", "dependency"]),
        )
        source = rng.choice(SOURCES)
        confidence = round(rng.uniform(0.5, 1.0), 4)
        props = cypher_props({"content": content, "source": source, "confidence": confidence})
        lines.append(f"CREATE (n:Fact {props});")

    lines.append("")

    # ── Entity nodes ─────────────────────────────────────────────────────────────
    lines.append("// ── Entity nodes ─────────────────────────────────────────────")
    for j in range(N_ENTITIES):
        name = rng.choice(ENTITIES_KG) + f"_{j}"
        etype = rng.choice(ENTITY_TYPES)
        props = cypher_props({"name": name, "type": etype})
        lines.append(f"CREATE (n:Entity {props});")

    return lines


# ── main ─────────────────────────────────────────────────────────────────────────

def write_fixture(path: str, lines: list[str]) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
        f.write("\n")
    node_lines = sum(1 for l in lines if l.startswith("CREATE"))
    print(f"wrote {path} ({node_lines} CREATE statements)")


def main() -> None:
    script_dir = os.path.dirname(os.path.abspath(__file__))
    repo_root = os.path.dirname(script_dir)
    fixtures_dir = os.path.join(repo_root, "fixtures", "real_world")

    social_path = os.path.join(fixtures_dir, "mini_social.cypher")
    knowledge_path = os.path.join(fixtures_dir, "mini_knowledge.cypher")

    print(f"generating social graph nodes (seed={SEED}) ...")
    social_lines = generate_social_nodes(seed=SEED)
    write_fixture(social_path, social_lines)

    print(f"generating knowledge graph nodes (seed={SEED}) ...")
    knowledge_lines = generate_knowledge_nodes(seed=SEED)
    write_fixture(knowledge_path, knowledge_lines)

    print("done.")


if __name__ == "__main__":
    main()
