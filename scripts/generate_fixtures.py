#!/usr/bin/env python3
"""Generate deterministic real-world fixture datasets for SparrowDB regression tests.

Produces two Cypher fixture files with node CREATE statements only.
Edge creation is handled in the Rust test code via WriteTx::create_edge,
since MATCH+CREATE edge patterns are not yet supported by the execution engine.

  fixtures/real_world/mini_social.cypher    — 1250 nodes (1000 Person, 50 Company, 200 Post)
  fixtures/real_world/mini_knowledge.cypher — 200 nodes (120 Fact, 80 Entity)

Run from the repository root:
  python3 scripts/generate_fixtures.py

The output is fully deterministic: same seed always produces the same files.
"""

import random
import os

# ── seed ────────────────────────────────────────────────────────────────────────
SEED = 42

# ── node counts ─────────────────────────────────────────────────────────────────
N_PERSONS = 1000
N_COMPANIES = 50
N_POSTS = 200
N_FACTS = 120
N_ENTITIES = 80


# ── helpers ─────────────────────────────────────────────────────────────────────

def esc(s: str) -> str:
    """Escape a string for use in a Cypher single-quoted literal."""
    return s.replace("\\", "\\\\").replace("'", "\\'")


def cypher_props(d: dict) -> str:
    """Render a dict as a Cypher property map literal."""
    parts = []
    for k, v in d.items():
        if isinstance(v, str):
            parts.append(f"{k}: '{esc(v)}'")
        elif isinstance(v, float):
            parts.append(f"{k}: {v:.4f}")
        else:
            parts.append(f"{k}: {v}")
    return "{" + ", ".join(parts) + "}"


